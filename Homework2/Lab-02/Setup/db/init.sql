CREATE TABLE ShelterOutcomes (
    AnimalID VARCHAR(10),
    Name VARCHAR(255),
    DateTime TIMESTAMP,
    MonthYear VARCHAR(10),
    DateOfBirth DATE,
    OutcomeType VARCHAR(50),
    OutcomeSubtype VARCHAR(50),
    AnimalType VARCHAR(50),
    SexUponOutcome VARCHAR(50),
    AgeUponOutcome VARCHAR(20),
    Breed VARCHAR(255),
    Color VARCHAR(50)
);
